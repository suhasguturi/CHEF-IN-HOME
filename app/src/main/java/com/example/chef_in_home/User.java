package com.example.chef_in_home;

public class User {
    public String name;
    public String email;
    public String role;
    public String contact;

    public User() {

    }

    public User(String name, String email, String role, String contact) {
        this.name = name;
        this.email = email;
        this.role = role;
        this.contact = contact;
    }
}
